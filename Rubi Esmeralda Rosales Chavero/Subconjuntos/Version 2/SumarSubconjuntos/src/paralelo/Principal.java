    private void Button_IniciarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_Button_IniciarActionPerformed
    {//GEN-HEADEREND:event_Button_IniciarActionPerformed
        if (datos_correctos())
        {
            Button_Iniciar.setEnabled(false);

            inicio_tiempo = System.nanoTime();

            int n = Integer.parseInt(TextField_n.getText().trim());
            int min = Integer.parseInt(TextField_min.getText().trim());
            int max = Integer.parseInt(TextField_max.getText().trim());

            int conjunto[] = genera_conjunto(n, min, max);
            TextField_Conjunto.setText(Arrays.toString(conjunto));

            TextField_Subconjuntos.setText(String.valueOf(total_subconjuntos((long) n)));

            for (int r = n; r >= 2; r--)
            {
                javax.swing.JTextArea ta = new JTextArea("Subconjuntos con " + r + " elementos\n\n");
                ta.setEditable(false);
                ta.setBackground(new Color(240, 240, 240));
                Panel_Subconjuntos.add(ta);
                Panel_Subconjuntos.updateUI();
                Thread t = new sumar_subconjuntos(TextArea_Resultados, ta, conjunto, r);
                t.start();
            }

        } else
        {
            JOptionPane.showMessageDialog(this, "Verifique los datos ingresados:\nn(mayor a 2)\nmin (negativo)\nmax (positivo)");
        }
    }//GEN-LAST:event_Button_IniciarActionPerformed



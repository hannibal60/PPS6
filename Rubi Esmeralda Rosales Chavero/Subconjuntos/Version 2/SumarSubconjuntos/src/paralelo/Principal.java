    private boolean datos_correctos()
    {
        boolean b = false;
        try
        {
            if ((Integer.parseInt(TextField_n.getText().trim())) > 2)
            {
                if (Integer.parseInt(TextField_min.getText().trim()) < 0)
                {
                    if (Integer.parseInt(TextField_max.getText().trim()) > 0)
                    {
                        b = true;
                    }
                }
            }
        } catch (Exception e)
        {
            System.out.println("Error de validación");
        }
        return b;
    }

    private int[] genera_conjunto(int n, int min, int max)
    {
        int conjunto[] = new int[n];
        for (int i = 0; i < n; i++)
        {
            conjunto[i] = (int) (Math.random() * (max - min + 1) + min);
        }
        return conjunto;
    }

    private long total_subconjuntos(long n)
    {
        long suma = 0;
        for (long r = n; r >= 2; r--)
        {
            long combinaciones = factorial(n) / (factorial(n - r) * factorial(r));
            System.out.println(combinaciones + " subconjuntos encontrados con " + r + " elementos");
            suma += combinaciones;
        }
        return suma;
    }

    private long factorial(long f)
    {
        if (f == 0)
        {
            return 1;
        } else
        {
            return f * factorial(f - 1);
        }
    }

